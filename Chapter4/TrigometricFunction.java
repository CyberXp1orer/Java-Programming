import java.util.Scanner;
public class TrigometricFunction {
    public static void main(String[] args) {
        System.out.println("The value of sin(PI/2):" +Math.sin(Math.PI/2));
        System.out.println("The value of exp(3.5)"+Math.exp(3.5));
        System.out.println("The value of power(2,3):"+Math.pow(2,3));
        System.out.println(Math.ceil(2.3));
        System.out.println(Math.floor(2.3));
        System.out.println(Math.rint(2.5));
        System.out.println(Math.rint(2.9));
        System.out.println(Math.rint(2.6));
    }
    
}

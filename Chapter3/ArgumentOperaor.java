public class ArgumentOperaor {
    public static void main(String[] args) {
        double a = 6.5;
        a = a + a + 6;
        System.out.println("The value of a without argument operator is :" + a);

        double b = 6.5;
        b += b + 6;
        System.out.println("he value of a with argument operator is :"+ b);

        double c = 4;
        c /= 2;
        System.out.println("he value of a with argument operator is :"+ c);
    }
    
    
}
